    # n = len(id) // 2
    # chunks = [id[:n], id[n:]]
    # if len(set(chunks)) == 1: return False
    for n in range(1, (len(id) // 2) + 1):
        chunks = [id[i : i + n] for i in range(0, len(id), n)]
        if len(set(chunks)) == 1: return False
    return True


